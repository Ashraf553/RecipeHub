// Запасные фото по категориям — проверенные «вкусные» снимки Unsplash.
// Используются, когда TheMealDB не вернул фото для блюда.
const categoryFallback = {
  breakfast: ['1517673400267-0251440c45dc', '1525351484163-7529414344d8'],
  soups: ['1547592166-23ac45744acd', '1571091718767-18b5b1457add'],
  main: ['1612874742237-6526221588e3', '1598103442097-8b74394b95c6'],
  salads: ['1551248429-40975aa4de74', '1546069901-ba9599a7e63c'],
  desserts: ['1571877227200-a0d98ea607e9', '1606313564200-e75d5e30476c'],
  baking: ['1555507036-ab1f4038808a', '1605522561233-768ad7a8fabf'],
  drinks: ['1505252585461-04db1eb84625', '1556679343-c7306c1976bc'],
  vegan: ['1512621776951-a57141f2eefd', '1512621776951-a57141f2eefd'],
};
const unsplash = (id) =>
  `https://images.unsplash.com/photo-${id}?auto=format&fit=crop&w=900&q=80`;

// Кеш фотографий из TheMealDB: имя блюда -> [url, ...].
const mealPhotos = {};
const prefetchPhotos = async () => {
  const names = [...new Set(Object.values(bases).flat().map((d) => d.name))];
  await Promise.all(
    names.map(async (name) => {
      const q = dishQuery[name];
      if (!q) return;
      try {
        const res = await fetch(
          `https://www.themealdb.com/api/json/v1/1/search.php?s=${encodeURIComponent(q)}`,
        );
        const data = await res.json();
        const urls = (data.meals || [])
          .map((m) => m.strMealThumb)
          .filter(Boolean);
        if (urls.length) mealPhotos[name] = urls;
      } catch {
        /* нет сети — используем запасное фото категории */
      }
    }),
  );
};

// Счётчики для равномерного перебора фото внутри блюда/категории.
const dishImgIdx = {};
const catImgIdx = {};
const imageFor = (dishName, cat) => {
  const photos = mealPhotos[dishName];
  if (photos && photos.length) {
    const i = (dishImgIdx[dishName] = (dishImgIdx[dishName] ?? 0) + 1) - 1;
    return photos[i % photos.length];
  }
  const pool = categoryFallback[cat];
  const i = (catImgIdx[cat] = (catImgIdx[cat] ?? 0) + 1) - 1;
  return unsplash(pool[i % pool.length]);
};

// Модификаторы названия для создания вариаций базового блюда.
const modifiers = [
  'классический', 'домашний', 'по-деревенски', 'острый', 'нежный',
  'пряный', 'сливочный', 'ароматный', 'праздничный', 'лёгкий',
  'сытный', 'быстрый', 'на скорую руку', 'с травами', 'с чесноком',
  'по-итальянски', 'по-домашнему', 'в духовке', 'на сковороде', 'постный',
];

const units = {
  default: 'г',
};
const unitFor = (name) => {
  const n = name.toLowerCase();
  if (/(молоко|сливки|бульон|вода|сок|вино|масло оливк|соевый|уксус|кокосовое мол)/.test(n)) return 'мл';
  if (/(яйца|яблоки|лимон|апельсин|банан|лук|морковь|перец|помидор|огурц|картофел|авокадо|желтк|зубчик)/.test(n)) return 'шт';
  if (/(мёд|сахар|паста|соус|масло|тахини|сметана|майонез|крахмал|какао)/.test(n)) return 'ст. л.';
  if (/(соль|корица|зира|паприка|ваниль|специ|кумин|кориандр|орегано|розмарин|тимьян|гвоздика)/.test(n)) return 'ч. л.';
  return 'г';
};
const amountFor = (name, servings) => {
  const u = unitFor(name);
  if (u === 'шт') return between(1, Math.max(2, Math.ceil(servings / 2) + 1));
  if (u === 'ст. л.') return between(1, 3);
  if (u === 'ч. л.') return between(1, 2);
  if (u === 'мл') return between(2, 5) * 50;
  return between(2, 8) * 50;
};

const slugify = (s) =>
  s.toLowerCase()
    .replace(/[а-яё]/g, (c) => {
      const map = { а:'a',б:'b',в:'v',г:'g',д:'d',е:'e',ё:'e',ж:'zh',з:'z',и:'i',й:'y',к:'k',л:'l',м:'m',н:'n',о:'o',п:'p',р:'r',с:'s',т:'t',у:'u',ф:'f',х:'h',ц:'c',ч:'ch',ш:'sh',щ:'sch',ъ:'',ы:'y',ь:'',э:'e',ю:'yu',я:'ya' };
      return map[c] ?? c;
    })
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');

const cookSteps = (dishName, ing) => {
  const a = pick(ing), b = pick(ing);
  return [
    {
      title: 'Основная готовка',
      description: `Соедините ${a.toLowerCase()} с остальными ингредиентами и готовьте на среднем огне, помешивая.`,
      durationMinutes: between(10, 30),
    },
    {
      title: 'Довести до готовности',
      description: `Добавьте ${b.toLowerCase()}, приправьте по вкусу и доведите блюдо «${dishName}» до полной готовности.`,
      durationMinutes: between(8, 25),
    },
  ];
};

const used = new Set();
const generated = [];

const categoryKeys = Object.keys(bases);
let safety = 0;

const makeRecipe = (cat) => {
  const base = pick(bases[cat]);
  const mod = pick(modifiers);
  const title = `${base.name} ${mod}`;
  let id = slugify(`${base.name}-${mod}`);
  let n = 2;
  let baseId = id;
  while (used.has(id)) id = `${baseId}-${n++}`;
  used.add(id);

  const servings = between(1, 6);
  const ingredients = base.ing.map((name) => ({
    name,
    amount: amountFor(name, servings),
    unit: unitFor(name),
  }));

  const steps = [prepStep(), ...cookSteps(base.name, base.ing), serveStep()];
  const [cMin, cMax] = base.cal;
  const calories = between(cMin, cMax);
  const protein = between(4, Math.max(8, Math.round(calories / 18)));
  const fat = between(4, Math.max(8, Math.round(calories / 22)));
  const carbs = Math.max(5, Math.round((calories - protein * 4 - fat * 9) / 4));

  return {
    id,
    title,
    description: `${base.name} (${mod}) — ${pick([
      'проверенный рецепт с понятными шагами и доступными продуктами.',
      'готовится из простых ингредиентов и отлично подходит для всей семьи.',
      'насыщенный вкус и аккуратная подача без лишних хлопот.',
      'сбалансированное блюдо, которое легко повторить дома.',
    ])}`,
    category: cat,
    cuisine: pick(cuisines),
    image: imageFor(base.name, cat),
    cookTimeMinutes: steps.reduce((s, st) => s + (st.durationMinutes ?? 0), 0),
    difficulty: pick(difficulties),
    rating: round1(between(40, 50) / 10),
    servings,
    ingredients,
    steps,
    nutrition: { calories, protein, fat, carbs },
    tags: mkTags(cat, cat === 'vegan' ? ['веганское'] : []),
  };
};

// Сколько рецептов уже есть в файле.
const fileSrc = readFileSync(recipesPath, 'utf8');
const existingCount = (fileSrc.match(/^\s{4}id: '/gm) || []).length;
const need = TARGET - existingCount;

// Извлечём существующие id, чтобы не плодить дубликаты.
for (const m of fileSrc.matchAll(/^\s{4}id: '([^']+)'/gm)) used.add(m[1]);

console.log('Загружаю фото блюд из TheMealDB...');
await prefetchPhotos();
console.log(`Получено фото для ${Object.keys(mealPhotos).length} блюд.`);

let catCursor = 0;
while (generated.length < need && safety++ < need * 50) {
  const cat = categoryKeys[catCursor++ % categoryKeys.length];
  generated.push(makeRecipe(cat));
}

// Сериализация в TS-литералы (компактно, в стиле файла).
const serializeRecipe = (r) => {
  const ing = r.ingredients
    .map((i) => `      { name: '${i.name}', amount: ${i.amount}, unit: '${i.unit}' },`)
    .join('\n');
  const steps = r.steps
    .map(
      (s) =>
        `      { title: '${s.title}', description: '${s.description.replace(/'/g, "\\'")}', durationMinutes: ${s.durationMinutes} },`,
    )
    .join('\n');
  const tags = r.tags.map((t) => `'${t}'`).join(', ');
  return `  {
    id: '${r.id}',
    title: '${r.title.replace(/'/g, "\\'")}',
    description:
      '${r.description.replace(/'/g, "\\'")}',
    category: '${r.category}',
    cuisine: '${r.cuisine}',
    image: '${r.image}',
    cookTimeMinutes: ${r.cookTimeMinutes},
    difficulty: '${r.difficulty}',
    rating: ${r.rating},
    servings: ${r.servings},
    ingredients: [
${ing}
    ],
    steps: [
${steps}
    ],
    nutrition: { calories: ${r.nutrition.calories}, protein: ${r.nutrition.protein}, fat: ${r.nutrition.fat}, carbs: ${r.nutrition.carbs} },
    tags: [${tags}],
  },`;
};

const block = generated.map(serializeRecipe).join('\n');

// Вставляем перед закрывающей `];` массива recipes.
const closeIdx = fileSrc.indexOf('\n];');
if (closeIdx === -1) throw new Error('Не найден конец массива recipes');
const out =
  fileSrc.slice(0, closeIdx) + '\n' + block + fileSrc.slice(closeIdx + 1);

writeFileSync(recipesPath, out, 'utf8');
console.log(
  `Было: ${existingCount}, добавлено: ${generated.length}, стало: ${existingCount + generated.length}`,
);
